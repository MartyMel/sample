package net.wmfs.coalesce.aa.access.vo;

public class Organisation {
	
	private String label;
	private long orgId;

	public String getLabel() {
		return label;
	}

	public long getLocId() {
		return orgId;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public void setLocId(long locId) {
		this.orgId = locId;
	}
}
